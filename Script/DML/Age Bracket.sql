DECLARE @DateofBirth DATE = GETDATE(); -- insert data here

SELECT
	@DateofBirth AS "Date of Birth",
	(0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 AS Age,
	CASE
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 0 AND 17 THEN 'BELOW 18'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 18 AND 20 THEN '18 - 20'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 20 AND 29 THEN '20 - 29'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 30 AND 39 THEN '30 - 39'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 40 AND 49 THEN '40 - 49'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 50 AND 59 THEN '50 - 59'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 60 AND 69 THEN '60 - 69'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 70 AND 79 THEN '70 - 79'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 80 AND 89 THEN '80 - 89'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 90 AND 99 THEN '90 - 99'
		WHEN (0 + CONVERT(CHAR(8),GETDATE(),112) - CONVERT(CHAR(8),@DateOfBirth,112)) / 10000 BETWEEN 100 AND 200 THEN '100 ABOVE'
		ELSE NULL
	END AS "Age Bracket";